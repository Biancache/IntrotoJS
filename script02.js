function compareNumbers(num1, num2) {
  if (num1 > num2) {
    alert("The first number is greater than the second number.");
  } else if (num1 < num2) {
    alert("The first number is less than the second number.");
  } else {
    alert("The numbers are equal.");
  }
}

compareNumbers(10, 5); 
compareNumbers(3, 7); 
compareNumbers(8, 8); 
